char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i] != (c % 256) && s[i] != '\0')
	{
		s++;
	}
	if (s[i] == '\0' && (c % 256) != '\0')
	{
		return (0);
	}
	return ((char *) s);
}
